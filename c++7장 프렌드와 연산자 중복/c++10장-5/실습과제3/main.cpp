#include <iostream> // 표준 입출력을 위한 헤더 파일
using namespace std; // 표준 네임스페이스 사용

int main() { 
    // 4x3 배열 선언 및 초기화 (마지막 행은 최대값 정보 저장용)
    int a[4][3] = {-5, 2, 35, -20, 5, 100, -75, 5, -25}; 

    int x = a[0][0]; // 최대값을 저장할 변수, 초기값은 배열의 첫 번째 원소
    for (int i = 0; i < 3; i++) { // 0번째부터 2번째 행까지 반복
        for (int j = 0; j < 3; j++) { // 0번째부터 2번째 열까지 반복
            if (x < a[i][j]) { // 현재 원소가 최대값보다 크면
                x = a[i][j]; // 최대값 갱신
                a[3][0] = x; // 최대값을 4번째 행 0번째 열에 저장
                a[3][1] = i; // 최대값의 행 번호를 4번째 행 1번째 열에 저장
                a[3][2] = j; // 최대값의 열 번호를 4번째 행 2번째 열에 저장
            }
        }
    }

    // 최대값 출력
    cout << "최대값은" << x << endl; 
    // 최대값의 위치 출력 (행과 열 번호는 1부터 시작하므로 +1)
    cout << "위치는 " << a[3][1] + 1 << "행 " << a[3][2] + 1 << "열"<<endl; 
}
